class Pessoa {
    private int id;
    private String nome;

    public void setId(int i) {
        this.id = i;
    }

    public void setNome(String n) {
        this.nome = n;
    }

    public int getId() {
        return this.id;
    }

    public String getNome() {
        return this.nome;
    }
}  
