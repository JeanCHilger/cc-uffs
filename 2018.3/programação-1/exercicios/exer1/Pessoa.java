class Pessoa {

    private int idade;
    private int anoNascimento;
    private double altura;

    public Pessoa() {
    }

    public int getIdade() {
        return this.idade;
    }

    public void setIdade(int newIdade) {
        this.idade = newIdade;
    }

    public int getAnoNascimento() {
        return this.anoNascimento;
    }

    public void setAnoNscimento(int newAnoNascimento) {
        this.anoNascimento = newAnoNascimento;
    }

    public double getAtura() {
        return this.altura;
    }

    public void setAltura(double newAltura) {
        this.altura = newAltura;        
    }
}

